/**
 * Writes android/local.properties with sdk.dir so Gradle finds the SDK without manual setup.
 * Uses ANDROID_HOME / ANDROID_SDK_ROOT, else common default paths per OS.
 */
const fs = require('fs');
const os = require('os');
const path = require('path');

const androidDir = path.join(__dirname, '..', 'android');
const outFile = path.join(androidDir, 'local.properties');

function findSdkDir() {
  const fromEnv = (process.env.ANDROID_HOME || process.env.ANDROID_SDK_ROOT || '').trim();
  if (fromEnv && fs.existsSync(fromEnv)) {
    return path.resolve(fromEnv);
  }

  const home = os.homedir();
  const candidates = [];
  if (process.platform === 'darwin') {
    candidates.push(path.join(home, 'Library', 'Android', 'sdk'));
  } else if (process.platform === 'win32') {
    if (process.env.LOCALAPPDATA) {
      candidates.push(path.join(process.env.LOCALAPPDATA, 'Android', 'Sdk'));
    }
    candidates.push(path.join(home, 'AppData', 'Local', 'Android', 'Sdk'));
  } else {
    candidates.push(path.join(home, 'Android', 'Sdk'));
  }

  for (const dir of candidates) {
    if (dir && fs.existsSync(dir)) {
      return path.resolve(dir);
    }
  }
  return null;
}

const sdkDir = findSdkDir();
if (!sdkDir) {
  console.error(
    '[android] Android SDK not found. Install Android Studio (SDK) or set ANDROID_HOME to your SDK path.',
  );
  process.exit(1);
}

const sdkDirPosix = sdkDir.replace(/\\/g, '/');
const contents = [
  '# Generated by scripts/write-android-local-properties.js — do not commit machine-specific paths.',
  `sdk.dir=${sdkDirPosix}`,
  '',
].join('\n');

fs.mkdirSync(androidDir, { recursive: true });
fs.writeFileSync(outFile, contents, 'utf8');
console.log('[android] sdk.dir ->', sdkDirPosix);
